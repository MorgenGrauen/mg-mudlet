function HilfeMudlet()

  faerbeText("script", [[
MUDLET SCHNELLEINSTIEG
----------------------

Mudlet bietet viel, um Dein Spiel einfacher und komfortabler zu gestalten.
Einige Funktionen haben wir schon mit diesem --> Paket an Skripten vorbereitet.
Unter anderem wird Dir eine --> Karte zeigen, wo Du dich im --> Spiel befindest.

Mudlet kannst Du im Menu auf Deutsch umstellen unter Optionen - Einstellungen.
Falls Du mehrere Charaktere spielen möchtest, erstelle ein weiteres Mudlet Profil.
Du kannst per "Multi View" auch mehrere Profile nebeneinander anzeigen lassen.

Im "Skript Editor" kannst Du selbst neue Skripte oder Funktionen ergänzen.
Am besten legst Du dazu neue Ordner an, und änderst nicht dieses MG-Paket.
Ansonsten könntest Du Deine Änderungen beim Update des Paketes verlieren.

Eine erste Anleitung findet sich auf der Mudlet Webseite in englischer Sprache.
Beispielsweise im Wiki sieht man ein ausführliches Tutorial und viele Beispiele.
Mudlet lässt sich in "Lua" programmieren. Das Wiki erklärt dazu viele API-Funktionen.
Mudlet zeigt in einigen Einführungsvideos auf Youtube, wie man einfache Dinge erledigt.

Ein "Alias" in Mudlet reagiert auf Deine Eingabe und sendet etwas anderes ans Spiel.
Ein "Trigger" in Mudlet reagiert auf Texte, die aus dem Spiel an Dich zurück kommen.
Man kann Trigger benutzen, um einfach Text einzufärben, oder komplexe Dinge zu tun.

Wenn Dein Lua-Code länger wird, solltest du Funktionen bauen und "Skripte" anlegen.
Falls du regelmäßig Code ausführen lassen willst, solltest Du die "Timer" anschauen.
Um bestimmte Funktionen sofort ausführen zu lassen, kannst Du "Tastenkürzel" anlegen.

Wie das konkret geht, lässt sich in diesem kurzen Text kaum abschließend beschreiben.
Man kann aber auch alles außerhalb von Mudlet in Lua-Textdateien programmieren.
Bei Interesse kannst Du Dir ja mal anschauen, wie bspw. dieses Paket so aufgebaut ist.

Für konkretere Fragen könntest Du im Spiel die Ebene "Mudlet" benutzen, bspw. so:
-mudlet Hallo Leute, wie kann ich den Mapper konfigurieren?

Es gibt zudem ein Discord vom MorgenGrauen (auf Deutsch) und von Mudlet (auf Englisch).
Hier kann man viele Spieler erreichen, Screenshots zeigen, ausführlich besprechen, usw.
Oder besuch mal das Morgengrauen auf Youtube und sieh Dir das ganze in Aktion an.

Egal, was Du in welcher Reihenfolge tust, wir wünschen Dir viel Spaß beim Erkunden! ;)

]])

end
